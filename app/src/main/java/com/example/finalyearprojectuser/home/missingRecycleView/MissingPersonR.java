package com.example.finalyearprojectuser.home.missingRecycleView;

public class MissingPersonR {
    String name;
    int imgPosition;

    public MissingPersonR() {

    }

    public MissingPersonR(String name, int imgPosition) {
        this.name = name;
        this.imgPosition = imgPosition;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImgPosition() {
        return imgPosition;
    }

    public void setImgPosition(int imgPosition) {
        this.imgPosition = imgPosition;
    }
}
